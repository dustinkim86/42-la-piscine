char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		j;
	char	*tmp;

	i = 0;
	j = 0;
	tmp = dest;
	while (tmp[i])
		i++;
	while (src[j])
	{
		tmp[i] = src[j];
		j++;
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
